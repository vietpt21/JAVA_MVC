public class KhuyenMai {
    int makhuyenmai;
    String tenkhuyenmai;
    String thoigianbatdau;
    String thoigianketthuc;
    String mota;
    public int getMakhuyenmai() {return makhuyenmai;}
    public void setMakhuyenmai(int makhuyenmai) {this.makhuyenmai = makhuyenmai;}
    public String getTenkhuyenmai() {return tenkhuyenmai;}
    public void setTenkhuyenmai(String tenkhuyenmai) {this.tenkhuyenmai = tenkhuyenmai;}
    public String getThoigianbatdau() {return thoigianbatdau;}
    public void setThoigianbatdau(String thoigianbatdau) {this.thoigianbatdau = thoigianbatdau;}
    public String getThoigianketthuc() {return thoigianketthuc;}
    public void setThoigianketthuc(String thoigianketthuc) {this.thoigianketthuc = thoigianketthuc;}
    public String getMota() {return mota;}
    public void setMota(String mota) {this.mota = mota;}
}
