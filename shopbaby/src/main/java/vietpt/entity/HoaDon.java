package vietpt.entity;

public class HoaDon {

    String mahoadon;
    String tenkhachhang;
    String sodienthoai;
    String diachigiaohang;
    boolean tinhtrang;
    String ngaymua;
    public String getMahoadon() {return mahoadon;}
    public void setMahoadon(String mahoadon) {this.mahoadon = mahoadon;}
    public String getTenkhachhang() {return tenkhachhang;}
    public void setTenkhachhang(String tenkhachhang) {this.tenkhachhang = tenkhachhang;}
    public String getSodienthoai() {return sodienthoai;}
    public void setSodienthoai(String sodienthoai) {this.sodienthoai = sodienthoai;}
    public String getDiachigiaohang() {return diachigiaohang;}
    public void setDiachigiaohang(String diachigiaohang) {this.diachigiaohang = diachigiaohang;}
    public boolean isTinhtrang() {return tinhtrang;}
    public void setTinhtrang(boolean tinhtrang) {this.tinhtrang = tinhtrang;}
    public String getNgaymua() {return ngaymua;}
    public void setNgaymua(String ngaymua) {this.ngaymua = ngaymua;}
}
